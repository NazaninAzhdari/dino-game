package body dino_pack is
    function pf_y_dino(velocity, y_dino: integer) return integer is
        variable gravity    :   integer  :=1;
        variable y_dino_next:   integer  :=y_dino;

        begin
            velocity   := velocity + gravity;
            y_dino_next:= y_dino + velocity;

            return y_dino_next;
        end function;

